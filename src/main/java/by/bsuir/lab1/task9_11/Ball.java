package by.bsuir.lab1.task9_11;

public class Ball {

    private final Color color;
    private final double weight;

    public Ball(Color color, int weight) {
        this.color = color;
        this.weight = weight;
    }

    public Color getColor() {
        return color;
    }

    public double getWeight() {
        return weight;
    }
}
